// brief.js — the graph→agent brief (Phase 2 "Open agent here", the Act plane).
// PURE + dependency-free so it runs identically in the browser, in server.js (Node),
// and under vitest. Given one node's /api/node payload it derives a working "brief":
// what-this-is / goals / now-next-blocked / key insights / links — written to
// <cwd>/SLIPWAY_BRIEF.md and loaded by claude-local as the agent's opening context.
// The graph is the source of truth; this is a point-in-time snapshot.

// Idea.status is free-form in the graph (~16 values), so bucketing needs a maintained map.
const NOW = new Set(['active', 'in_progress', 'in-progress', 'exploring', 'doing']);
const NEXT = new Set(['proposed', 'next_up', 'planned', 'accepted', 'validated', 'todo', 'open']);
const BLOCKED = new Set(['blocked', 'deferred', 'open_question', 'on_hold', 'paused']);
const DONE = new Set(['implemented', 'done', 'shipped', 'complete', 'completed', 'archived']);

// Bucket ideas into now / next / blocked from their status. Done ideas are dropped
// (they're not work-to-do); unknown/empty statuses fall through to "next".
export function bucketIdeas(ideas = []) {
  const now = [], next = [], blocked = [];
  for (const i of ideas) {
    const s = String(i.status || '').toLowerCase().trim();
    if (DONE.has(s)) continue;
    if (NOW.has(s)) now.push(i);
    else if (BLOCKED.has(s)) blocked.push(i);
    else next.push(i);            // proposed/unknown/empty → next
  }
  return { now, next, blocked };
}

// Best-effort local-path hint for a node, from its /api/node payload. Projects rarely
// carry file_path directly (only one does), so we also mine the node's git_repo Sources
// and any edge that exposes a file_path (e.g. a related Project). The SERVER resolves
// this hint to a real, in-sandbox repo directory; a null hint means "no local dir".
export function cwdHint(node = {}) {
  if (node.file_path) return node.file_path;
  const srcs = node.all_sources || [];
  const git = srcs.find((s) => s && s.source_kind === 'git_repo' && s.file_path);
  if (git) return git.file_path;
  const anySrc = srcs.find((s) => s && s.file_path);
  if (anySrc) return anySrc.file_path;
  const edge = (node.edges || []).find((e) => e && e.file_path);
  if (edge) return edge.file_path;
  return null;
}

// Map a raw /api/node payload → the flat shape buildBriefMarkdown wants. Pure: it only
// reads edges/all_sources already present on the node (no extra query needed).
export function deriveBriefInput(node = {}) {
  const edges = node.edges || [];
  const byLabel = (lbl) => edges.filter((e) => e && e.label === lbl);
  return {
    name: node.name || 'Untitled',
    label: node.label || 'node',
    description: node.desc || node.description || '',
    status: node.status || '',
    domain: (node.props && node.props.domain) || node.domain || '',
    tags: node.tags || (node.props && node.props.tags) || [],
    goals: byLabel('Goal').map((e) => ({ name: e.name, status: e.status })),
    ideas: byLabel('Idea').map((e) => ({ name: e.name, status: e.status })),
    // Insight edges carry the summary as their coalesced name.
    insights: byLabel('Insight').map((e) => e.name).filter(Boolean),
    sources: (node.all_sources || []).map((s) => ({ name: s.name, url: s.url, file_path: s.file_path, kind: s.source_kind })),
  };
}

const bullet = (s) => `- ${s}`;

// Compose the SLIPWAY_BRIEF.md markdown from a derived input. `cwd` is the resolved
// working directory (shown in the footer). Everything is optional and degrades to a
// short honest note rather than an empty section.
export function buildBriefMarkdown(input = {}, cwd = '') {
  const { name, label, description, status, domain, tags = [], goals = [], ideas = [], insights = [], sources = [] } = input;
  const { now, next, blocked } = bucketIdeas(ideas);
  const L = [];

  L.push(`# ${name} — agent brief`);
  L.push('');
  L.push('> Generated by Scatterbrained "Open agent here" from the knowledge graph. A point-in-time');
  L.push('> snapshot — the graph is the source of truth. Edit there, not here.');
  L.push('');

  L.push('## What this is');
  const meta = [label, status, domain].filter(Boolean).join(' · ');
  if (meta) L.push(`*${meta}*`);
  L.push(description ? description.trim() : '_No description recorded in the graph._');
  if (Array.isArray(tags) && tags.length) L.push(`\nTags: ${tags.join(', ')}`);
  L.push('');

  L.push('## Goals');
  if (goals.length) goals.forEach((g) => L.push(bullet(`**${g.name}**${g.status ? ` (${g.status})` : ''}`)));
  else L.push('_No goals linked. Consider defining one._');
  L.push('');

  L.push('## Now / Next / Blocked');
  L.push('### Now');
  if (now.length) now.forEach((i) => L.push(bullet(`**${i.name}**${i.status ? ` (${i.status})` : ''}`)));
  else L.push('_nothing in progress_');
  L.push('### Next');
  if (next.length) next.forEach((i) => L.push(bullet(`${i.name}${i.status ? ` (${i.status})` : ''}`)));
  else L.push('_nothing queued_');
  L.push('### Blocked / open questions');
  if (blocked.length) blocked.forEach((i) => L.push(bullet(`${i.name}${i.status ? ` (${i.status})` : ''}`)));
  else L.push('_nothing blocked_');
  L.push('');

  if (insights.length) {
    L.push('## Key insights');
    insights.slice(0, 8).forEach((s) => L.push(bullet(String(s).replace(/\s+/g, ' ').trim())));
    L.push('');
  }

  if (sources.length) {
    L.push('## Links');
    sources.slice(0, 12).forEach((s) => {
      const ref = s.url || s.file_path;
      L.push(bullet(ref ? `${s.kind ? s.kind + ': ' : ''}[${s.name}](${ref})` : s.name));
    });
    L.push('');
  }

  L.push('---');
  L.push(`_Working dir: ${cwd || '(unresolved)'} · re-run "Open agent here" to refresh._`);
  return L.join('\n') + '\n';
}
